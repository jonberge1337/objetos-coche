package minumero;

public class Minumero {
	private int numero;
	public Minumero(int numero) {
		this.numero = numero;
		
	}
	public int getDoble() {
		int doble;
		doble = numero * 2;
		return doble;
	}
	
	public int getTriple() {
		int triple;
		triple = numero * 3;
		return triple;
	}
	
	public int getCuadruple() {
		int cuadruple;
		cuadruple = numero * 4;
		return cuadruple;
	}
	
	
}
